-- 内容页面表结构
CREATE TABLE IF NOT EXISTS content_pages (
    id INT AUTO_INCREMENT PRIMARY KEY,
    page_key VARCHAR(50) NOT NULL UNIQUE COMMENT '页面标识',
    title VARCHAR(100) NOT NULL COMMENT '页面标题',
    content TEXT NOT NULL COMMENT '页面内容',
    is_active BOOLEAN DEFAULT TRUE COMMENT '是否启用',
    deleted_at DATETIME NULL COMMENT '逻辑删除时间',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='内容页面表';

