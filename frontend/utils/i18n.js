/**
 * 国际化管理器
 * 支持中文、英文、日语
 */

const { get } = require('./request')

// 支持的语言列表
const LANGUAGES = [
  { code: 'zh-CN', name: '简体中文', nativeName: '简体中文' },
  { code: 'en-US', name: 'English', nativeName: 'English' },
  { code: 'ja-JP', name: '日本語', nativeName: '日本語' }
]

// 默认语言
const DEFAULT_LANGUAGE = 'zh-CN'

// 当前语言
let currentLanguage = DEFAULT_LANGUAGE

// 文本缓存 - 初始化时立即设置默认值
let textCache = getDefaultTexts()

// 监听器列表
const listeners = []

// 初始化完成标志
let initialized = false

/**
 * 获取存储的语言
 */
function getStoredLanguage() {
  try {
    return wx.getStorageSync('language') || DEFAULT_LANGUAGE
  } catch (e) {
    return DEFAULT_LANGUAGE
  }
}

/**
 * 设置存储的语言
 */
function setStoredLanguage(languageCode) {
  try {
    wx.setStorageSync('language', languageCode)
  } catch (e) {
    console.error('保存语言设置失败', e)
  }
}

/**
 * 初始化国际化
 */
async function initI18n() {
  currentLanguage = getStoredLanguage()
  // 立即设置默认文本，确保页面有内容显示
  textCache = getDefaultTexts()
  notifyListeners()

  // 然后尝试从服务器加载
  await loadTexts()
  initialized = true
}

/**
 * 加载文本
 */
async function loadTexts() {
  try {
    const data = await get(`/i18n/texts/${currentLanguage}`)
    if (data && data.texts) {
      // 合并服务器返回的文本和默认文本（服务器文本优先）
      textCache = { ...getDefaultTexts(), ...data.texts }
      notifyListeners()
    }
  } catch (e) {
    console.error('加载国际化文本失败，使用默认文本', e)
    // 请求失败时保持使用默认文本
    textCache = getDefaultTexts()
    notifyListeners()
  }
}

/**
 * 获取文本
 * @param {string} key 文本键
 * @param {object} params 替换参数
 */
function t(key, params = {}) {
  let text = textCache[key] || key

  // 替换参数
  Object.keys(params).forEach(k => {
    text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), params[k])
  })

  return text
}

/**
 * 切换语言
 */
async function setLanguage(languageCode) {
  if (currentLanguage === languageCode) return

  currentLanguage = languageCode
  setStoredLanguage(languageCode)

  // 立即更新默认文本
  textCache = getDefaultTexts()
  notifyListeners()

  // 然后从服务器加载
  await loadTexts()
}

/**
 * 获取当前语言
 */
function getLanguage() {
  return currentLanguage
}

/**
 * 获取支持的语言列表
 */
function getLanguages() {
  return LANGUAGES
}

/**
 * 添加监听器
 */
function addListener(callback) {
  listeners.push(callback)
  // 立即通知一次当前状态
  if (textCache && Object.keys(textCache).length > 0) {
    callback(currentLanguage, textCache)
  }
  return () => {
    const index = listeners.indexOf(callback)
    if (index > -1) {
      listeners.splice(index, 1)
    }
  }
}

/**
 * 通知监听器
 */
function notifyListeners() {
  listeners.forEach(callback => callback(currentLanguage, textCache))
}

/**
 * 获取文本缓存（供 behavior 使用）
 */
function getTextCache() {
  return textCache
}

